/// Converts [x1, y1, x2, y2] format to Rect (x, y, width, height)
pub fn bbox_to_rect(bbox: &[i32; 4]) -> Rect {
    Rect::new(
        bbox[0] as f32,
        bbox[1] as f32,
        (bbox[2] - bbox[0]) as f32,
        (bbox[3] - bbox[1]) as f32,
    )
}
